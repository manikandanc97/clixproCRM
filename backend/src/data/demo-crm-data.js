function monthDate(monthOffset, day, hour = 10) {
  const today = new Date();
  const safeDay = Math.min(Math.max(day, 1), 28);

  return new Date(today.getFullYear(), today.getMonth() + monthOffset, safeDay, hour, 0, 0, 0);
}

function dayDate(dayOffset, hour = 10) {
  const date = new Date();
  date.setDate(date.getDate() + dayOffset);
  date.setHours(hour, 0, 0, 0);

  return date;
}

